#include "../inc/push_swap_bonus.h"

void	rb(t_list *d)
{
	int	temp;
	int	i;

	temp = d->sb[0];
	i = -1;
	while (++i < (d->sib - 1))
		d->sb[i] = d->sb[i + 1];
	d->sb[d->sib - 1] = temp;
}
